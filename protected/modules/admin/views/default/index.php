<h1>Добро пожаловать в административную панель!</h1>
<p>Для того, чтобы просмотреть заказы, выберите в меню "Заказы"</p>
<p>Для того, чтобы просмотреть заказы подарочных квестов, выберите в меню "Квест в подарок"</p>
